// Define get_crc based on CPU calcs
crc_t get_crc(const uint8_t *data_array, uint32_t data_len, crc_t crc_start)
{
    uint8_t i;
    const uint8_t *data_p = data_array;

    while (data_len--)
    {
        crc_start ^= *data_p++;
        for (i = 0; i < 8; i++)
        {
            if (crc_start & 0x1)
                crc_start ^= crc_poly;
            crc_start >>= 1;
        }
    }

    return crc_start;
}

